-- Looking total cases vs Population

SELECT location, date, total_cases, population, (total_cases/population)*100 as Infected
From [Portfolio Project]..CovidDeaths
Where location like '%uzbekistan%'
Order by 1,2

-- Looking at Countries with highest infection

SELECT location, Population, Max(total_cases) as HighestInfection
From [Portfolio Project]..CovidDeaths
Group by location,Population
Order by HighestInfection Desc

-- Looking Countries with Highest Death Count by Population

